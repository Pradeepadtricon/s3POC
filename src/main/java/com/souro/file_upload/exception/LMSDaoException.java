package com.souro.file_upload.exception;

@SuppressWarnings("serial")
public class LMSDaoException extends Exception {

	public LMSDaoException() {
		super();
		
	}

	public LMSDaoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public LMSDaoException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public LMSDaoException(String message) {
		super(message);
		
	}

	public LMSDaoException(Throwable cause) {
		super(cause);
		
	}

}
